package com.arpico.groupit.usermanagement.dto;

public class SbuDto {

	private String sbuId;
	private String sbuDescription;
	private String sbuShortName;

	public String getSbuId() {
		return sbuId;
	}

	public void setSbuId(String sbuId) {
		this.sbuId = sbuId;
	}

	public String getSbuDescription() {
		return sbuDescription;
	}

	public void setSbuDescription(String sbuDescription) {
		this.sbuDescription = sbuDescription;
	}

	public String getSbuShortName() {
		return sbuShortName;
	}

	public void setSbuShortName(String sbuShortName) {
		this.sbuShortName = sbuShortName;
	}

}
